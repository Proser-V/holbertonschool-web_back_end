#!/usr/bin/env python3
"""
A module for a type-annotated function add that takes a float a and
a float b as arguments and returns their sum as a float.
"""


def add(a: float, b: float) -> float:
    """
    Type-annotated function add that takes a float a and a float b as
    arguments and returns their sum as a float.
    """
    return a + b
